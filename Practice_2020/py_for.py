# 下面使用for循环来打印魔术师名单中的所有名字：
# 冒号不能忘了
# 这些命名约定有助于你明白for循环中将对每个元素执行的操作。使用单数和复数式名称，
# 可帮助你判断代码段处理的是单个列表元素还是整个列表。
print("下面使用for循环来打印魔术师名单中的所有名字：\n冒号不能忘了")
persons = ['chen', 'lin', 'wang', 'gong']
for person in persons:
    print(person)
    print(person + ' is my Little brother !')
print('Thanks to every brother for his wonderful performance')

